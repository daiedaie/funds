package com.mrbt.details.model;

public class IndusAllCom {
	/**
	 * 行业代码
	 */
	private String indusCode;
	/**
	 * 行业名称
	 */
	private String indusName;
	/**
	 * 基金行业配置
	 */
	private String indusConf;
	/**
	 * 同类平均
	 */
	private String indusAge;
	/**
	 * 基金行业超配
	 */
	private String indusSper;
	public String getIndusCode() {
		return indusCode;
	}
	public void setIndusCode(String indusCode) {
		this.indusCode = indusCode;
	}
	public String getIndusName() {
		return indusName;
	}
	public void setIndusName(String indusName) {
		this.indusName = indusName;
	}
	public String getIndusConf() {
		return indusConf;
	}
	public void setIndusConf(String indusConf) {
		this.indusConf = indusConf;
	}
	public String getIndusAge() {
		return indusAge;
	}
	public void setIndusAge(String indusAge) {
		this.indusAge = indusAge;
	}
	public String getIndusSper() {
		return indusSper;
	}
	public void setIndusSper(String indusSper) {
		this.indusSper = indusSper;
	}
}
